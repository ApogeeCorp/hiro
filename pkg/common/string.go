package common

// CLen gets the C-string length
func CLen(n []byte) int {
	for i := 0; i < len(n); i++ {
		if n[i] == 0 {
			return i
		}
	}
	return len(n)
}

// CTrim trims a c function
func CTrim(str string) string {
	bstr := []byte(str)
	nullIndex := CLen(bstr)
	bstr = bstr[:nullIndex]
	return string(bstr)
}
